/* ── Cart helpers ──────────────────────────────────────────────────── */

const CART_KEY = 'memelli_cart';

function getCart(): { productId: string; qty: number }[] {
  if (typeof window === 'undefined') return [];
  try {
    return JSON.parse(localStorage.getItem(CART_KEY) || '[]');
  } catch {
    return [];
  }
}

function addToCart(productId: string) {
  const cart = getCart();
  const existing = cart.find((c) => c.productId === productId);
  if (existing) {
    existing.qty += 1;
  } else {
    cart.push({ productId, qty: 1 });
  }
  localStorage.setItem(CART_KEY, JSON.stringify(cart));
}

/* ── Cell Renderer ─────────────────────────────────────────────────── */

function ComparisonCell({ value }: { value: boolean | string }) {
  if (value === true) {
    return (
      <div className="flex items-center justify-center">
        <div className="flex h-6 w-6 items-center justify-center rounded-full bg-emerald-500/15 border border-emerald-500/20">
          <Check className="h-3.5 w-3.5 text-emerald-400" />
        </div>
      </div>
    );
  }
  if (value === false) {
    return <div className="flex items-center justify-center"><span className="text-muted-foreground">&mdash;</span></div>;
  }
  return <div className="flex items-center justify-center"><span className="text-xs font-medium text-muted-foreground">{value}</span></div>;
}

